# Variables and Constants
PI = 3.14159  # Constant
radius = 5    # Variable

# Data Types
integer_value = 10         # Integer
float_value = 10.5         # Float
string_value = "Hello!"    # String
boolean_value = True       # Boolean

# Type Conversion
int_to_float = float(integer_value)  # Integer to Float
float_to_int = int(float_value)      # Float to Integer
int_to_string = str(integer_value)   # Integer to String

# Output
print("PI (Constant):", PI)
print("Radius (Variable):", radius)
print("Integer to Float:", int_to_float)
print("Float to Integer:", float_to_int)
print("Integer to String:", int_to_string)
